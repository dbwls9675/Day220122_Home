package SocketChatting;

import java.io.BufferedReader;
import java.io.BufferedWriter;
import java.io.IOException;
import java.io.InputStreamReader;
import java.io.OutputStreamWriter;
import java.net.ServerSocket;
import java.net.Socket;
import java.util.Scanner;

// 소켓을 이용한 서버/클라이언트 채팅 예제
public class ServerEx {

	public static void main(String[] args) {
		BufferedReader in = null;
		BufferedWriter out = null;
		ServerSocket listener = null;
		Socket socket = null;
		Scanner scan = new Scanner(System.in);

		try {
			listener = new ServerSocket(9999);
			System.out.println("연결을 기달리고 있습니다.");
			socket = listener.accept();
			System.out.println("연결을 받았습니다.");
			in = new BufferedReader(new InputStreamReader(socket.getInputStream()));
			out = new BufferedWriter(new OutputStreamWriter(socket.getOutputStream()));

			while (true) {
				String inputMessage = in.readLine();
				if (inputMessage.equalsIgnoreCase("bye")) {
					System.out.println("클라이언트에서 bye로 연결을 끊었습니다.");
					break;
				}
				System.out.println("클라이언트 : " + inputMessage);
				System.out.print("보내기>> ");
				String outputMessage = scan.next();
				out.write(outputMessage + "\n");
				out.flush();
			}
		} catch (IOException e) {
			System.out.println(e.getMessage());
		} finally {
			try {
				scan.close();
				socket.close();
				listener.close();
			} catch (Exception e) {
				System.out.println("클라이언트아 채팅 중 오류 발생");
			}
		}
	}

}
